    private void displayPurchaseHistory() {
    JPanel contentPanel = new JPanel();
    contentPanel.setLayout(new BoxLayout(contentPanel, BoxLayout.Y_AXIS));

    List<Purchase> purchaseList = Identification.getUsers().get(whatnum).getPurchases();

    if (purchaseList.isEmpty()) {
        JLabel label = new JLabel("No purchase history yet.");
        label.setForeground(Color.BLACK);
        contentPanel.add(label);
    } else {
        for (Purchase p : purchaseList) {
            JPanel card = new JPanel();
            card.setLayout(new BorderLayout());
            card.setBorder(BorderFactory.createLineBorder(Color.BLACK));
            card.setMaximumSize(new Dimension(480, 120));  // size for card

            // Movie image
            JLabel imageLabel = new JLabel();
            ImageIcon icon = new ImageIcon(p.getImagePath());
            Image img = icon.getImage().getScaledInstance(100, 100, Image.SCALE_SMOOTH);
            imageLabel.setIcon(new ImageIcon(img));

            // Info
            JPanel infoPanel = new JPanel(new GridLayout(3, 1));
            infoPanel.add(new JLabel("Movie: " + p.getMovieTitle()));
            infoPanel.add(new JLabel("Showtime: " + p.getShowtime()));

            // Receipt button
            JButton receiptButton = new JButton("View Receipt");
            receiptButton.addActionListener(e -> {
                String message = "Movie: " + p.getMovieTitle() + 
                                "\nShowtime: " + p.getShowtime() +
                                "\nSeats: " + String.join(", ", p.getSeats()) +
                                "\nTotal Price: â‚±" + String.format("%,d", p.getTotalPrice());
                JOptionPane.showMessageDialog(this, message, "Receipt", JOptionPane.INFORMATION_MESSAGE);
            });

            card.add(imageLabel, BorderLayout.WEST);
            card.add(infoPanel, BorderLayout.CENTER);
            card.add(receiptButton, BorderLayout.EAST);

            contentPanel.add(card);
            contentPanel.add(Box.createVerticalStrut(10)); // space between cards
        }
    }

    jScrollPane2.setViewportView(contentPanel);
}

