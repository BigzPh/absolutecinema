private void displayPaymentDetails(List<String> selectedSeats, int totalPrice) {
    StringBuilder sb = new StringBuilder();
    if (jScrollPane1.getVerticalScrollBar().isVisible()) {
    sb.append("*********************************************************************\n");
    sb.append("                                           RECEIPT                       \n");
    sb.append("*********************************************************************\n");
} else {
    sb.append("*************************************************************************\n");
    sb.append("                                           RECEIPT                       \n");
    sb.append("*************************************************************************\n");
}

    sb.append("Selected Seats:\n");
    for (String seat : selectedSeats) {
        sb.append("- ").append(seat).append("\n");
    }
    sb.append("*************************************************************************\n");
    sb.append("Total Price: â‚±").append(String.format("%,d", totalPrice));
    jTextArea1.setText(sb.toString());
}

